num_1 = int(input("ingrese primer numero: "))
num_2 = int(input("ingrese segundo numero: "))
son_iguales = num_1 == num_2
print(f"Los numeros son iguales: {son_iguales}")

son_distintos = num_1 != num_2
print(f"Los numeros son distintos: {son_distintos}")

es_mayor = num_1 > num_2
print(f"El primer numero es mayor: {es_mayor}")

es_menor_o_igual = num_1 <= num_2
print(f"El primer numero es menor o igual: {es_menor_o_igual} ")